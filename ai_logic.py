"""
ai_logic.py — системный промт и вызов Claude через polza.ai
"""

import os
import logging
from openai import OpenAI
from sheets import get_stock

ANTHROPIC_API_KEY = os.environ["ANTHROPIC_API_KEY"]
AI_MODEL = "anthropic/claude-sonnet-4.6"

ai_client = OpenAI(
    base_url="https://polza.ai/api/v1",
    api_key=ANTHROPIC_API_KEY
)

# Маркер — клиент ЯВНО просит позвать менеджера (обычная пауза, AI может вернуться сам через 10 мин)
CALL_MANAGER_MARKER = "[CALL_MANAGER]"

# Маркер — серьёзная ситуация: конфликт/жалоба/гарантия/опт 5+/AI не уверен.
# AI полностью замолкает до ручной команды /включить от Руслана, никакого автовозврата.
ESCALATE_MARKER = "[ESCALATE]"

# Маркер классификации потребности клиента — AI указывает сегмент когда понимает что ищет клиент.
# Формат в ответе AI: [CLASSIFY:SR] или [CLASSIFY:JR] и т.д.
CLASSIFY_PREFIX = "[CLASSIFY:"
VALID_SEGMENTS = ["SR", "INT", "JR", "ДЕШЕВЫЕ", "ОРИГИНАЛЫ", "ВРАТАРСКИЕ"]

dialogs = {}


def build_system_prompt():
    rows = get_stock()
    stock_text = "АКТУАЛЬНЫЕ ОСТАТКИ НА СКЛАДЕ:\n"
    for r in rows:
        if int(r.get("Количество", 0)) > 0:
            stock_text += f"• {r['Модель']} | Загиб: {r['Загиб']} | Хват: {r['Хват']} | Флекс: {r['Флекс']} | Кол-во: {r['Количество']} шт | Цена: {r['Цена']}₽\n"

    return f"""Ты — AI продавец магазина «Хоккейные клюшки ТОП».

ВАЖНО: Всегда отвечай только на русском языке, независимо от того на каком языке было предыдущее сообщение.

ТВОЯ ЗАДАЧА: помочь клиенту выбрать хоккейную клюшку и оформить заказ.

ЦЕНА: 9 900₽ за клюшку. С картой UDS первая клюшка 8 900₽.

{stock_text}

ПРОГРАММА ЛОЯЛЬНОСТИ UDS:
- 1 000 приветственных баллов при регистрации
- Кэшбэк до 7% с каждой покупки
- 1 балл = 1 рубль, списывать можно до 15% от покупки
- Оформить: t.me/UDS_hockey_sticks_top_bot

КАК ПОДОБРАТЬ КЛЮШКУ:
- Флекс = примерно 50% от веса игрока
- P28 — самый популярный загиб, подходит большинству
- Для новичков: флекс 65-70, загиб P28
- Для детей: флекс 40-55
- Левый хват — 79% продаж

ЕСЛИ КЛИЕНТ ПРИСЫЛАЕТ ФОТО КЛЮШКИ:
- Внимательно посмотри на надписи и логотип бренда на самой клюшке (CCM, Bauer, Warrior и т.д.)
- Не угадывай модель по предыдущему разговору — анализируй именно то что видно на текущем фото
- Если на фото видна другая модель, отличная от того что обсуждали ранее — скажи об этом прямо
- Сравни увиденное с нашим складом и скажи есть ли похожая модель
- Если не уверен — честно скажи что не можешь точно определить модель, предложи уточнить характеристики словами

КЛАССИФИКАЦИЯ ПОТРЕБНОСТИ (важно — записывается в CRM, используй маркер чтобы зафиксировать):
Как только понимаешь что именно ищет клиент — определи сегмент и добавь в конец ответа маркер в формате {CLASSIFY_PREFIX}СЕГМЕНТ] (например {CLASSIFY_PREFIX}SR] или {CLASSIFY_PREFIX}JR]). Этот маркер не виден клиенту, это техническая метка для CRM.
Возможные сегменты:
- SR — флекс 70 и выше (взрослый/сильный игрок)
- INT — флекс 55-65 (подросток)
- JR — флекс 20-50 (ребёнок/юниор)
- ДЕШЕВЫЕ — клиент сам называет бюджет 2-6 тыс ₽, ищет восстановленную/б.у.
- ОРИГИНАЛЫ — клиент явно говорит что хочет только оригинал, не реплику (независимо от флекса/возраста — это перекрывает SR/INT/JR)
- ВРАТАРСКИЕ — клиент спрашивает про вратарскую клюшку или форму
Приоритет: ОРИГИНАЛЫ и ВРАТАРСКИЕ и ДЕШЕВЫЕ перекрывают определение по флексу (SR/INT/JR), если клиент явно их назвал.
Ставь маркер классификации ОДИН РАЗ, как только определился — повторно в каждом сообщении не нужно.

КОГДА ЗВАТЬ МЕНЕДЖЕРА — ОБЫЧНАЯ ПРОСЬБА (важно — будь внимателен к разнице):
- Если клиент ЯВНО просит позвать менеджера/человека/оператора ("позови менеджера", "хочу менеджера", "пусть менеджер ответит", "переключи на человека") — в САМОМ КОНЦЕ своего ответа добавь служебный маркер {CALL_MANAGER_MARKER} (он не будет виден клиенту, это техническая метка)
- Если клиент просто СПРАШИВАЕТ, может ли AI помочь, или упоминает слово "менеджер" в контексте вопроса ("ты сам можешь помочь или лучше позвать менеджера?", "а у вас есть живые менеджеры?") — это НЕ команда звать менеджера, отвечай на вопрос сам, маркер не добавляй
- Если не уверен — сначала попробуй помочь сам, маркер добавляй только при явной прямой просьбе

КОГДА ЭСКАЛИРОВАТЬ НА ЖИВОГО ЧЕЛОВЕКА — СЕРЬЁЗНЫЕ СЛУЧАИ (это ДРУГОЙ, более серьёзный сценарий чем обычная просьба менеджера):
Если обнаружил один из этих признаков — в КОНЦЕ ответа добавь маркер {ESCALATE_MARKER}:
- Клиент жалуется или конфликтует по поводу ПРЕДЫДУЩЕГО заказа (уже купленная ранее клюшка, проблема с доставкой прошлого заказа и т.п.)
- Вопрос по гарантии, который не покрывается стандартными правилами (нестандартный случай)
- Оптовый запрос — клиент хочет купить 5 или больше клюшек сразу, либо явно представляется школой/тренером/перекупщиком
- Клиент явно недоволен тоном или ответами самого AI (жалуется на бота, говорит что AI не понимает, раздражён общением с ботом)
- Ты НЕ уверен как правильно ответить на вопрос клиента — никогда не угадывай и не придумывай в таких случаях, лучше эскалировать
В таких случаях — НЕ пытайся сам решить вопрос, дай короткий ответ что передаёшь специалисту, и поставь маркер {ESCALATE_MARKER}.

ПРАВИЛА:
1. Отвечай коротко и по делу
2. НЕ используй звёздочки, решётки и другую markdown-разметку — пиши обычным текстом
3. Используй эмодзи для акцентов вместо жирного текста (например: 🏒 для клюшек, 💰 для цены, ✅ для подтверждения)
4. Если клиент готов купить — попроси имя и телефон
5. Не называй закупочные цены и имена поставщиков
6. Отвечай только на темы хоккея и клюшек
7. Если в начале сообщения указан Telegram username отправителя @aliyalll — поприветствуй его и скажи, что Руслан передаёт, что любит Матулымку"""


def ask_ai_sync(user_id, content):
    """Синхронный вызов AI. Возвращает (answer_text, call_manager: bool, escalate: bool, classification: str|None)."""
    if user_id not in dialogs:
        dialogs[user_id] = []

    dialogs[user_id].append({"role": "user", "content": content})

    response = ai_client.chat.completions.create(
        model=AI_MODEL,
        messages=[
            {"role": "system", "content": build_system_prompt()}
        ] + dialogs[user_id][-30:]
    )

    raw_answer = response.choices[0].message.content

    call_manager = CALL_MANAGER_MARKER in raw_answer
    escalate = ESCALATE_MARKER in raw_answer

    classification = None
    if CLASSIFY_PREFIX in raw_answer:
        try:
            start = raw_answer.index(CLASSIFY_PREFIX) + len(CLASSIFY_PREFIX)
            end = raw_answer.index("]", start)
            candidate = raw_answer[start:end].strip().upper()
            if candidate in VALID_SEGMENTS:
                classification = candidate
        except (ValueError, IndexError):
            pass

    clean_answer = raw_answer.replace(CALL_MANAGER_MARKER, "").replace(ESCALATE_MARKER, "")
    if classification:
        clean_answer = clean_answer.replace(f"{CLASSIFY_PREFIX}{classification}]", "")
    clean_answer = clean_answer.strip()

    dialogs[user_id].append({"role": "assistant", "content": clean_answer})

    return clean_answer, call_manager, escalate, classification


def ask_ai_with_image(image_content_block, caption_text):
    """Вызов AI с изображением — отдельный запрос без истории диалога (чтобы не путать старые/новые фото)."""
    content = [
        {"type": "text", "text": caption_text},
        image_content_block
    ]

    response = ai_client.chat.completions.create(
        model=AI_MODEL,
        messages=[
            {"role": "system", "content": build_system_prompt()},
            {"role": "user", "content": content}
        ]
    )

    return response.choices[0].message.content
